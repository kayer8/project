export interface BuildingImportItem {
  buildingNo: number;
  roomNos: string[];
  paidRooms: string[];
}

function buildRooms(startFloor: number, endFloor: number, unitsPerFloor: number, skip: string[] = []) {
  const roomNos: string[] = [];

  for (let floorNo = startFloor; floorNo <= endFloor; floorNo += 1) {
    for (let unitNo = 1; unitNo <= unitsPerFloor; unitNo += 1) {
      const roomNo = `${floorNo}${String(unitNo).padStart(2, '0')}`;
      if (!skip.includes(roomNo)) {
        roomNos.push(roomNo);
      }
    }
  }

  return roomNos;
}

